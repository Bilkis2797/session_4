package VariousConcepts;

public class LearnTypeCasting {

	public static void main(String[] args) {
		
		//widening casting
		int a = 5;
		double b = a;
		System.out.println(a);
		System.out.println(b);
		
		//narrowing casting
		double c = 7.5;
		int d = (int) c;
		
		System.out.println(c);
		System.out.println(d);

	}

}
